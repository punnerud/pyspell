/// The request body: everything after the `\r\n\r\n` header terminator. Empty
/// if there is no body (e.g. a GET) or the terminator isn't in this segment.
fn http_body(req: &[u8]) -> &[u8] {
    let n = req.len();
    let mut i = 0;
    while i + 4 <= n {
        if &req[i..i + 4] == b"\r\n\r\n" {
            return &req[i + 4..];
        }
        i += 1;
    }
    &[]
}

/// First request line (up to CR/LF) as a str.
fn first_line(req: &[u8]) -> &str {
    let end = req
        .iter()
        .position(|&b| b == b'\r' || b == b'\n')
        .unwrap_or(req.len());
    core::str::from_utf8(&req[..end]).unwrap_or("")
}

/// Value of `key` in an `a=b&c=d` query string, or "".
fn query_get<'a>(query: &'a str, key: &str) -> &'a str {
    for kv in query.split('&') {
        if let Some((k, v)) = kv.split_once('=') {
            if k == key {
                return v;
            }
        }
    }
    ""
}

/// Percent-decode a query value (`+` -> space, `%XX` -> byte), UTF-8 lossy.
fn url_decode(s: &str) -> String {
    let b = s.as_bytes();
    let mut out: Vec<u8> = Vec::with_capacity(b.len());
    let mut i = 0;
    while i < b.len() {
        match b[i] {
            b'+' => {
                out.push(b' ');
                i += 1;
            }
            b'%' if i + 2 < b.len() => match (hexval(b[i + 1]), hexval(b[i + 2])) {
                (Some(h), Some(l)) => {
                    out.push(h * 16 + l);
                    i += 3;
                }
                _ => {
                    out.push(b'%');
                    i += 1;
                }
            },
            c => {
                out.push(c);
                i += 1;
            }
        }
    }
    String::from_utf8_lossy(&out).into_owned()
}

fn hexval(c: u8) -> Option<u8> {
    match c {
        b'0'..=b'9' => Some(c - b'0'),
        b'a'..=b'f' => Some(c - b'a' + 10),
        b'A'..=b'F' => Some(c - b'A' + 10),
        _ => None,
    }
}

/// IPv4 / generic 16-bit one's-complement checksum.
fn checksum(data: &[u8]) -> u16 {
    finish(sum16(data, 0))
}

/// TCP checksum over the pseudo-header + TCP segment.
fn tcp_checksum(src: &[u8; 4], dst: &[u8; 4], tcp: &[u8]) -> u16 {
    let mut s: u32 = 0;
    let mut ph = [0u8; 12];
    ph[0..4].copy_from_slice(src);
    ph[4..8].copy_from_slice(dst);
    ph[9] = PROTO_TCP;
    ph[10..12].copy_from_slice(&(tcp.len() as u16).to_be_bytes());
    s = sum16(&ph, s);
    s = sum16(tcp, s);
    finish(s)
}

fn sum16(data: &[u8], mut sum: u32) -> u32 {
    let mut i = 0;
    while i + 1 < data.len() {
        sum += u16::from_be_bytes([data[i], data[i + 1]]) as u32;
        i += 2;
    }
    if i < data.len() {
        sum += (data[i] as u32) << 8;
    }
    sum
}

fn finish(mut sum: u32) -> u16 {
    while sum >> 16 != 0 {
        sum = (sum & 0xffff) + (sum >> 16);
    }
    !(sum as u16)
}

fn rand_u32() -> u32 {
    let mut b = [0u8; 4];
    getrandom::getrandom(&mut b).expect("getrandom");
    u32::from_le_bytes(b)
}
